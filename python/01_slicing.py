 # String slicing
name = "John Smith"
print(name[0:4])    # will get  "john" from index 0 to 3 (4 is exclusive)  
print(name[5:])      
print(name[:4])      
print(name[-5:])     
print(name[::-1])    

# List slicing
numbers = [10, 20, 30, 40, 50]
print(numbers[1:3])     
print(numbers[::2])    
print(numbers[::-1])    #will get  [50, 40, 30, 20, 10]  → reversed

